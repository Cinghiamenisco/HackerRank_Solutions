if __name__ == '__main__':
    n = int(input())
    student_marks = {}
    for _ in range(n):
        name, *line = input().split()
        scores = list(map(float, line))
        student_marks[name] = scores
        
    query_name = input()
    tot = 0.00
    count = 0
    for m in student_marks[query_name]:
        tot+=m
        count+=1
        
    print("%.2f" % (tot/count))
        
